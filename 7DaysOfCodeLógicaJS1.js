// Declaração de variáveis
// Aqui, declaramos seis variáveis, três delas são números e três são strings
let numeroUm = 1;         // Variável do tipo número com valor 1
let stringUm = '1';       // Variável do tipo string com valor '1'
let numeroTrinta = 30;    // Variável do tipo número com valor 30
let stringTrinta = '30';  // Variável do tipo string com valor '30'
let numeroDez = 10;       // Variável do tipo número com valor 10
let stringDez = '10';     // Variável do tipo string com valor '10'

// Comparando 'numeroUm' e 'stringUm'
if (numeroUm === stringUm) {
  // Usando === para verificar se os valores E os tipos são iguais
  console.log('As variáveis numeroUm e stringUm tem o mesmo valor e mesmo tipo');
} else if (numeroUm == stringUm) {
  // Usando == para verificar se os valores são iguais, independente do tipo
  console.log('As variáveis numeroUm e stringUm tem o mesmo valor, mas tipos diferentes');
} else {
  // Caso em que nem os valores nem os tipos são iguais
  console.log('As variáveis numeroUm e stringUm não tem o mesmo valor');
}

